import turtle

s = turtle.Pen()
e = turtle.Pen()
x = turtle.Pen()
turtle.bgcolor('black')
s.color('red')
e.color('red')
x.color('red')
s.width(5)
e.width(5)
x.width(5)

s.penup()
s.goto(-200, -100)
s.pendown()
s.circle(75, 180)
s.circle(-75, 180)

e.penup()
e.goto(0, -100)
e.pendown()
e.rt(180)
e.circle(-100, 270)
e.rt(90)
e.fd(200)

x.penup()
x.goto(150, -100)
x.pendown()
x.lt(45)
x.fd(250)
x.lt(135)
x.penup()
x.fd(200)
x.lt(135)
x.pendown()
x.fd(250)

turtle.exitonclick()
